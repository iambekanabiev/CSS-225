# to convert radians to degrees

import math


radians_angle = float(input("Enter an angle in radians: "))

degrees_angle = math.degrees(radians_angle)

# Print the input value and the calculated value in degrees
print(f"Input value in radians: {radians_angle}")
print(f"Calculated value in degrees: {degrees_angle}")